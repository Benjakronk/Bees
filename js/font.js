// cache of pre-rendered glyph sheets, one per color
const _fontSheets = {};
const _fontOrder = Object.keys(FONT_GLYPHS);
const _fontIndex = {};
_fontOrder.forEach((c, i) => { _fontIndex[c] = i; });

function _buildSheet(color) {
  const can = document.createElement('canvas');
  can.width = _fontOrder.length * FONT_W;
  can.height = FONT_H;
  const c = can.getContext('2d');
  c.fillStyle = color;
  for (let i = 0; i < _fontOrder.length; i++) {
    const rows = FONT_GLYPHS[_fontOrder[i]];
    for (let y = 0; y < FONT_H; y++) {
      const bits = rows[y];
      for (let x = 0; x < FONT_W; x++) {
        if (bits & (1 << (FONT_W - 1 - x))) c.fillRect(i * FONT_W + x, y, 1, 1);
      }
    }
  }
  return can;
}

function drawText(ctx, str, x, y, color, scale) {
  scale = scale || 1;
  let sheet = _fontSheets[color];
  if (!sheet) sheet = _fontSheets[color] = _buildSheet(color);
  str = String(str).toUpperCase();
  let cx = x;
  for (let i = 0; i < str.length; i++) {
    const ch = str[i];
    if (ch === '\n') { y += FONT_LINE * scale; cx = x; continue; }
    const gi = _fontIndex[ch];
    if (gi === undefined) { cx += FONT_ADV * scale; continue; }
    ctx.drawImage(sheet, gi * FONT_W, 0, FONT_W, FONT_H,
      cx, y, FONT_W * scale, FONT_H * scale);
    cx += FONT_ADV * scale;
  }
  return cx;
}

function textWidth(str, scale) {
  scale = scale || 1;
  let max = 0, w = 0;
  for (const ch of String(str)) {
    if (ch === '\n') { max = Math.max(max, w); w = 0; continue; }
    w += FONT_ADV * scale;
  }
  return Math.max(max, w) - (max === 0 && w > 0 ? scale : 0);
}

// centered text
function drawTextC(ctx, str, cx, y, color, scale) {
  drawText(ctx, str, Math.floor(cx - textWidth(str, scale) / 2), y, color, scale);
}

// text with hard drop shadow, very DOS
function drawTextS(ctx, str, x, y, color, scale) {
  drawText(ctx, str, x + (scale || 1), y + (scale || 1), '#000000', scale);
  drawText(ctx, str, x, y, color, scale);
}
function drawTextCS(ctx, str, cx, y, color, scale) {
  scale = scale || 1;
  const x = Math.floor(cx - textWidth(str, scale) / 2);
  drawTextS(ctx, str, x, y, color, scale);
}
